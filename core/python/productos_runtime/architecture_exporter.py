    def generate_pm_briefing(
        self,
        architecture: Dict[str, Any],
        gaps: List[Dict[str, Any]],
        forecast: Dict[str, Any],
        domain_activation: Dict[str, Any],
    ) -> str:
        """12. PM Briefing — structured summary with confidence levels."""
        return self._export_pm_briefing(architecture, gaps, forecast, domain_activation)

    def _export_pm_briefing(
        self,
        architecture: Dict[str, Any],
        gaps: List[Dict[str, Any]],
        forecast: Dict[str, Any],
        domain_activation: Dict[str, Any],
    ) -> str:
        """Generate PM-facing briefing document."""
        persona_pack = architecture.get("persona_archetype_pack", {})
        workflow_map = architecture.get("workflow_orchestration_map", {})
        num_archetypes = len(persona_pack.get("archetypes", []))
        num_handoffs = len(workflow_map.get("handoffs", []))
        top_gaps = sorted(gaps, key=lambda x: {"critical": 0, "warning": 1, "info": 2}.get(x.get("severity", "info"), 3))[:5]
        bottlenecks = forecast.get("bottleneck_rankings", [])[:3]

        lines = [
            "# PM Briefing",
            "",
            f"**Architecture:** {architecture.get('product_architecture_id', 'N/A')}",
            f"**Generated:** {datetime.now(timezone.utc).isoformat()}",
            "",
            "## What You Asked For",
            f"- Intent decomposed into {num_archetypes} personas, {num_handoffs} handoffs",
            f"- Domain: {domain_activation.get('domain', 'general')} (confidence: {domain_activation.get('confidence', 0.0):.0%})",
            "",
            "## What We Built",
            f"- {len(architecture.get('artifacts', []))} artifacts generated",
            f"- {len(architecture.get('cross_links', []))} cross-layer links",
            f"- {len(architecture.get('component_prds', []))} component PRDs",
            f"- {len(architecture.get('customer_journey_maps', []))} customer journey maps",
            "",
            "## What We Found",
            f"- **Top gaps:** {len(top_gaps)} critical/warning gaps detected",
            f"- **Top bottlenecks:** {len(bottlenecks)} bottlenecks predicted",
            "",
        ]

        if top_gaps:
            lines.append("### Top Gaps\n")
            for g in top_gaps:
                lines.extend([
                    f"- **[{g['severity'].upper()}]** {g['description'][:100]}",
                    f"  - Impact: {g['impact'][:100]}",
                    f"  - Suggestion: {g['suggestion'][:100]}",
                    "",
                ])

        if bottlenecks:
            lines.append("### Top Bottlenecks\n")
            for b in bottlenecks:
                lines.append(f"- {b['impact_description'][:120]}\n")

        lines.extend([
            "## Confidence Report",
            "",
            "| Claim | Confidence | Provenance |",
            "|---|---|---|",
            f"| Domain classification | {domain_activation.get('confidence', 0.0):.0%} | observed |",
            f"| Persona identification | {num_archetypes} personas | inferred |",
            f"| Workflow handoffs | {num_handoffs} handoffs | inferred |",
            f"| Bottleneck prediction | {len(bottlenecks)} bottlenecks | simulated |",
            "",
            "## Requires Your Confirmation",
            "",
        ])

        for g in top_gaps:
            if g.get("confidence", 1.0) < 0.5:
                lines.append(f"- ⚠️ {g['description'][:100]}\n")

        if not any(g.get("confidence", 1.0) < 0.5 for g in top_gaps):
            lines.append("No low-confidence inferences requiring confirmation.\n")

        lines.extend([
            "## Next Actions",
            "1. Review gaps and accept/dismiss suggestions",
            "2. Run what-if simulation on top bottleneck",
            "3. Confirm domain activation and regions",
            "4. Review PM briefing confidence levels",
            "",
            "---",
            "*Generated by ProductOS V14 ArchitectureExporter — PM Briefing*",
        ])

        return "\n".join(lines)
