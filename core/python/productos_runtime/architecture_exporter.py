    def _export_executive_summary(self, architecture: Dict[str, Any]) -> str:
        """6. Executive summary PDF (markdown placeholder)."""
        workflow_map = architecture.get("workflow_orchestration_map", {})
        persona_pack = architecture.get("persona_archetype_pack", {})
        num_personas = len(persona_pack.get("archetypes", []))
        num_handoffs = len(workflow_map.get("handoffs", []))

        summary_lines = [
            "Executive Summary - Product Architecture",
            f"Architecture ID: {architecture.get('product_architecture_id', 'N/A')}",
            f"Personas: {num_personas}",
            f"Handoffs: {num_handoffs}",
            f"Components: {len(architecture.get('component_prds', []))}",
            f"Journey Maps: {len(architecture.get('customer_journey_maps', []))}",
            f"API Contracts: {len(architecture.get('api_contract_hypotheses', []))}",
            "",
            "Key Insights",
            "1. Multi-persona workflow orchestration with cross-layer traceability",
            "2. Compliance gates and regulatory overlays are explicitly surfaced",
            "3. The architecture is ready for simulation, review, and PM briefing",
        ]
        return self._render_minimal_pdf("\n".join(summary_lines))

    def _render_minimal_pdf(self, text: str) -> str:
        lines = [line[:90] for line in text.splitlines() if line.strip()]
        if not lines:
            lines = ["ProductOS Executive Summary"]
        content_lines = ["BT", "/F1 12 Tf", "50 780 Td"]
        for index, line in enumerate(lines):
            safe = line.replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)")
            if index == 0:
                content_lines.append(f"({safe}) Tj")
            else:
                content_lines.append("0 -18 Td")
                content_lines.append(f"({safe}) Tj")
        content_lines.append("ET")
        stream = "\n".join(content_lines) + "\n"

        objects = [
            "1 0 obj\n<< /Type /Catalog /Pages 2 0 R >>\nendobj\n",
            "2 0 obj\n<< /Type /Pages /Count 1 /Kids [3 0 R] >>\nendobj\n",
            "3 0 obj\n<< /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] /Resources << /Font << /F1 4 0 R >> >> /Contents 5 0 R >>\nendobj\n",
            "4 0 obj\n<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>\nendobj\n",
            f"5 0 obj\n<< /Length {len(stream.encode('ascii'))} >>\nstream\n{stream}endstream\nendobj\n",
        ]

        pdf_parts = ["%PDF-1.4\n"]
        offsets = [0]
        current_length = len(pdf_parts[0].encode("ascii"))
        for obj in objects:
            offsets.append(current_length)
            pdf_parts.append(obj)
            current_length += len(obj.encode("ascii"))

        xref_offset = current_length
        xref = [f"xref\n0 {len(offsets)}\n", "0000000000 65535 f \n"]
        for offset in offsets[1:]:
            xref.append(f"{offset:010d} 00000 n \n")
        trailer = (
            f"trailer\n<< /Size {len(offsets)} /Root 1 0 R >>\n"
            f"startxref\n{xref_offset}\n%%EOF\n"
        )
        pdf_parts.extend(xref)
        pdf_parts.append(trailer)
        return "".join(pdf_parts)

    def _export_mermaid_diagram(self, architecture: Dict[str, Any]) -> str:
        """7. Technical architecture diagram in Mermaid format."""
        lines = ["graph TD"]
        persona_pack = architecture.get("persona_archetype_pack", {})
        workflow_map = architecture.get("workflow_orchestration_map", {})
        component_prds = architecture.get("component_prds", [])

        for a in persona_pack.get("archetypes", []):
            pid = a["persona_ref"].replace("-", "_")
            lines.append(f"    {pid}[{a.get('label', pid)}]")

        for h in workflow_map.get("handoffs", []):
            src = h["source_persona"].replace("-", "_")
            tgt = h["target_persona"].replace("-", "_")
            lines.append(f"    {src} -->|{h.get('shared_artifact', 'handoff')}| {tgt}")

        for c in component_prds:
            cid = c.get("component_prd_id", "").replace("-", "_")[:20]
            lines.append(f"    {cid}[{c.get('component_name', cid)[:30]}]")

        return "\n".join(lines)

    def _export_analytics_spec(self, architecture: Dict[str, Any]) -> str:
        """8. Analytics instrumentation spec."""
        workflow_map = architecture.get("workflow_orchestration_map", {})
        handoffs = workflow_map.get("handoffs", [])

        events = []
        for h in handoffs:
            events.append({
                "event": f"handoff.{h['handoff_id']}.initiated",
                "trigger": f"{h['source_persona']} initiates {h['shared_artifact']}",
                "properties": ["handoff_id", "source_persona", "target_persona", "timestamp"],
            })
            events.append({
                "event": f"handoff.{h['handoff_id']}.completed",
                "trigger": f"{h['target_persona']} receives {h['shared_artifact']}",
                "properties": ["handoff_id", "source_persona", "target_persona", "duration_ms", "timestamp"],
            })

        return json.dumps({
            "event_taxonomy": events,
            "metrics": [
                {"name": "handoff_completion_time", "aggregation": "p50/p90/p95", "alert_threshold": "p95 > SLA"},
                {"name": "bottleneck_frequency", "aggregation": "count_per_handoff", "alert_threshold": ">10%"},
            ],
            "dashboards": [
                {"audience": "PM", "widgets": ["handoff_completion_times", "bottleneck_heatmap", "sla_violation_rate"]},
                {"audience": "executive", "widgets": ["overall_workflow_health", "bottleneck_summary"]},
            ],
        }, indent=2)

